package JavaPrograms;

public class CheckingNumberIsEvenOrOdd 
{
	public static void main(String[] args) 
	{
		int number=7;
		System.out.println("Program For Checking Number is Even Or Odd Number");
		System.out.println("===========");
		if(number%2==0)
			System.out.println(number+" is a Even Number");
		else
		    System.out.println(number+" is a Odd Number");
	}

}
